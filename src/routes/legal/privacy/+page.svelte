<h1>Privacy Policy for The Nerds Catalog</h1>
<p>
    This Privacy Policy explains how information is handled when you use The Nerds Catalog.
</p>

<ol>
    <li>
        <strong>Our Commitment to Your Privacy:</strong> The Nerds Catalog is designed with your privacy in mind. While we do not employ tracking cookies, analytics tools, or any other mechanisms to directly collect personal information about you for general Browse, we do utilize a cookie for specific functionalities related to your shopping experience.
    </li>
    <li>
        <strong>Information We Collect Directly and by Third Parties:</strong>
        <ul>
            <li>
                <strong>Shopping Cart Information (via Cookie):</strong> When you add items to your shopping cart, a cookie is used to store the contents of your cart on your device. This cookie is essential for the functionality of our shopping cart and allows us to retain your selected items as you navigate the site. The contents of this cookie are sent to our backend system and then to Doku Merchant for checkout and payment processing. This cookie does not contain personally identifiable information directly, but it links to your chosen items for purchase.
            </li>
            <li>
                <strong>Checkout Information:</strong> During the checkout process, we collect the following personal information directly from you:
                <ul>
                    <li><strong>Nickname:</strong> This is used to personalize your order and for internal identification.</li>
                    <li><strong>Phone Number:</strong> This is essential for order processing, communication regarding your order, and specifically for preorder pickup purposes. If you place a preorder, your phone number may be added to a group (e.g., WhatsApp, Telegram) with other individuals who have preordered the same item(s) to facilitate pickup arrangements and notifications.</li>
                    <li><strong>Email Address:</strong> This is used for order confirmation, communication regarding your order, and to send you updates related to your purchase.</li>
                </ul>
            </li>
            <li>
                <strong>Payment Information:</strong> When you make a purchase, your payment details are securely processed by <strong>Doku Merchant</strong>. We do not have access to your raw payment card information. Doku Merchant's privacy practices govern the collection and use of your financial data. We only receive confirmation from Doku Merchant regarding the success or failure of your payment.
            </li>
            <li>
                <strong>Usage Data (via Hosting):</strong> Our hosting provider, <strong>Netlify</strong>, automatically collects basic, non-identifiable log data (such as your IP address, browser type, and pages visited) that is typical for website operation. This data is used for server maintenance, security, and understanding general site traffic patterns, not for identifying individual users.
            </li>
        </ul>
    </li>
    <li>
        <strong>How We Use Your Information:</strong>
        <ul>
            <li>To process your orders and transactions.</li>
            <li>To communicate with you regarding your purchases, including order confirmations, updates, and customer service inquiries.</li>
            <li>To facilitate preorder pickups by adding your phone number to a group with other preordering customers for coordination.</li>
            <li>To improve our website functionality and user experience.</li>
            <li>For internal record-keeping and administrative purposes.</li>
        </ul>
    </li>
    <li>
        <strong>Sharing of Your Information:</strong>
        We share your personal information only as necessary to fulfill your orders and operate our services:
        <ul>
            <li><strong>Doku Merchant:</strong> Your cart contents, nickname, phone number, and email address are shared with Doku Merchant to facilitate payment processing.</li>
            <li><strong>Preorder Pickup Groups:</strong> Your phone number may be shared with other customers who have preordered the same items to create a group for pickup coordination. By placing a preorder, you consent to this limited sharing of your phone number for this specific purpose.</li>
            <li>We do not sell, rent, or otherwise disclose your personal information to third parties for their marketing purposes.</li>
        </ul>
    </li>
    <li>
        <strong>Data Retention Policy:</strong>
        <ul>
            <li><strong>Directly Collected Data:</strong> We retain the personal information you provide during checkout (nickname, phone number, email address) for as long as necessary to fulfill the purposes for which it was collected, including customer service, warranty support, and compliance with legal obligations.</li>
            <li><strong>Third-Party Data:</strong> Any data collected by our third-party service providers (Doku Merchant, Netlify) is retained according to their respective data retention policies. The Nerds Catalog does not directly retain any raw payment card information.</li>
        </ul>
    </li>
    <li>
        <strong>User Rights:</strong> While The Nerds Catalog directly holds some of your personal data (nickname, phone number, email), you may have rights regarding your data (such as access, correction, or deletion) with both us and our third-party service providers. To inquire about the personal information we hold about you, or to request corrections or deletion, please contact us using the "Contact Us" section below. Please refer to Doku Merchant's and Netlify's privacy policies for information on how to exercise your rights regarding data they collect.
    </li>
    <li>
        <strong>Security Measures:</strong> We and our third-party service providers implement reasonable security measures designed to protect the information processed through our website from unauthorized access, alteration, disclosure, or destruction. We utilize secure payment gateways and strive to maintain appropriate safeguards for all data we handle.
    </li>
    <li>
        <strong>External Links:</strong> Our website may contain links to external sites that are not operated by us. Please be aware that we have no control over the content and practices of these sites and cannot accept responsibility or liability for their respective privacy policies. We encourage you to review the privacy policies of any third-party sites you visit.
    </li>
    <li>
        <strong>Children's Privacy:</strong> Our website is not intended for children under the age of 13, and we do not knowingly collect any personal information from children under 13. If we become aware that we have inadvertently received personal information from a child under 13, we will delete such information from our records.
    </li>
    <li>
        <strong>Changes to the Privacy Policy:</strong> We may update our Privacy Policy from time to time to reflect changes in our practices or for other operational, legal, or regulatory reasons. We will notify you of any significant changes by posting the new Privacy Policy on this page with a revised "Last Updated" date. You are advised to review this Privacy Policy periodically for any changes. Your continued use of the website after any modifications to this Privacy Policy will constitute your acknowledgment of the modifications and your consent to abide and be bound by the modified Privacy Policy.
    </li>
    <li>
        <strong>Contact Us:</strong> If you have any questions about this Privacy Policy, please contact us through the provided channels on our website.
    </li>
</ol>

